async def main():
    bot = Bot(token=TOKEN)
    now = datetime.utcnow().replace(second=0, microsecond=0)
    current_time = now.strftime("%H:%M")

    tasks = []
    if current_time in schedule:
        for uid in USER_IDS:
            message = random.choice(schedule[current_time])
            tasks.append(send(bot, uid, message))

    if tasks:
        await asyncio.gather(*tasks)

